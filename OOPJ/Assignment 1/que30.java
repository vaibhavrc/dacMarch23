/*
Write a program to find minimum and maximum number as well as to add two double numbers using methods of Double.
*/

class que30{
    public static void main(String args[]){
        double a=5102.03040;
        double b=62030.405060;
        System.out.println("Max. of two numbers is: "+Double.max(a,b));
        System.out.println("Min. of two numbers is: "+Double.min(a,b));
        System.out.println("Sum of two numbers is: "+Double.sum(a,b));
    }
}
/*
Max. of two numbers is: 62030.40506
Min. of two numbers is: 5102.0304
Sum of two numbers is: 67132.43546
*/