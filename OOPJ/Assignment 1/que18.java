/*
Write a program to find minimum and maximum number as well as to add two integer numbers using methods of Integer.
*/

class que18{
    public static void main(String args[]){
        int a=10;
        int b=20;
        System.out.println("Max. of two numbers is: "+Integer.max(a,b));
        System.out.println("Min. of two numbers is: "+Integer.min(a,b));
        System.out.println("Sum of two numbers is: "+Integer.sum(a,b));
    }
}
/*
Max. of two numbers is: 20
Min. of two numbers is: 10
Sum of two numbers is: 30
*/