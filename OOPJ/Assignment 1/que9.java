//Write a program to convert state of Byte instance into byte, short, int. long, float and double
class que9{
    public static void main(String args[]){
        Byte b1=new Byte("12");
        System.out.println("Covert into Byte: "+((byte)b1));
        System.out.println("Covert into Short: "+((short)b1));
        System.out.println("Covert into Int: "+((int)b1));
        System.out.println("Covert into Long: "+((long)b1));
        System.out.println("Covert into Float: "+((float)b1));
        System.out.println("Covert into Double: "+((double)b1));
    }
}
